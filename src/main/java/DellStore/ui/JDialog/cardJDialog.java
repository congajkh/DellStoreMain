    private void tbl_bangcardMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_bangcardMouseClicked
        int row = tbl_bangcard.getSelectedRow();
        if (row >= 0) {
            card item = listCARD.get(row);
            txt_tencard.setText(item.getTen());
            txt_loai.setText(item.getLoai());
            if (item.getTrang_thai() == 1) {
                rdo_conhang.setSelected(true);
            } else {
                rdo_hethang.setSelected(true);
            }
        }
    }//GEN-LAST:event_tbl_bangcardMouseClicked

