    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String ten = txt_tencard.getText().trim();
        String loai = txt_loai.getText().trim();
        int trangThai = rdo_conhang.isSelected() ? 1 : 0;

        card newCARD = new card();
        newCARD.setTen(ten);
        newCARD.setLoai(loai);
        newCARD.setTrang_thai(trangThai);

        cardDAOImpl dao = new cardDAOImpl();
        dao.insert(newCARD); // hàm insert trong DAO của bạn
        JOptionPane.showMessageDialog(this, "Thêm CPU thành công!");
        loadTableCARD(); // cập nhật lại bảng
        clearForm(); // xóa form nếu muốn
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        int row = tbl_bangcard.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn CPU để sửa!");
            return;
        }
        // Lấy object cpu theo chỉ số dòng được chọn
        card cardUpdate = listCARD.get(row);

        // Lấy dữ liệu từ form
        String ten = txt_tencard.getText().trim();
        String tocDo = txt_loai.getText().trim();
        String loai = txt_loai.getText().trim();
        int trangThai = rdo_conhang.isSelected() ? 1 : 0;

        // Cập nhật thông tin vào object
        cardUpdate.setTen(ten);
        cardUpdate.setLoai(loai);
        cardUpdate.setTrang_thai(trangThai);
        // Gọi DAO để cập nhật
        cardDAOImpl dao = new cardDAOImpl();
        dao.update(cardUpdate);

        JOptionPane.showMessageDialog(this, "Cập nhật thành công!");

        loadTableCARD();  // reload bảng sau khi sửa
        clearForm();     // xóa trắng form (nếu có)
    }//GEN-LAST:event_btn_suaActionPerformed

