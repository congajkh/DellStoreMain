  private void loadTableCARD() {
    cardDAOImpl dao = new cardDAOImpl();
    listCARD = dao.findAll();

    DefaultTableModel model = (DefaultTableModel) tbl_bangcard.getModel();
    model.setRowCount(0);
    int stt = 1;
    for (card item : listCARD) {
        model.addRow(new Object[] {
            stt++,
            item.getTen(),
            item.getLoai(),
            item.getTrang_thai() == 1 ? "Còn hàng" : "Hết hàng"
        });
    }
}
